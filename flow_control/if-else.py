percentage = int(input("Enter the percentage: "))
if 91 <= percentage <= 100:
    print("Grade A")
elif 81 <= percentage <= 90:
    print("Grade B")
elif 71 <= percentage <= 80:
    print("Grade C")
elif 61 <= percentage <= 70:
    print("Grade D")
elif 51 <= percentage <= 60:
    print("Grade E")
else:
    print("Grade F")
